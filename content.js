export function cloneData(value) {
  return JSON.parse(JSON.stringify(value));
}

function mergeWithDefaults(defaultValue, incomingValue) {
  if (Array.isArray(defaultValue)) {
    if (!Array.isArray(incomingValue)) {
      return cloneData(defaultValue);
    }

    if (!defaultValue.length) {
      return cloneData(incomingValue);
    }

    return incomingValue.map((item) =>
      mergeWithDefaults(defaultValue[0], item)
    );
  }

  if (defaultValue && typeof defaultValue === "object") {
    const source =
      incomingValue && typeof incomingValue === "object" ? incomingValue : {};
    const mergedValue = {};

    Object.keys(defaultValue).forEach((key) => {
      mergedValue[key] = mergeWithDefaults(defaultValue[key], source[key]);
    });

    return mergedValue;
  }

  if (typeof defaultValue === "string") {
    return typeof incomingValue === "string" ? incomingValue : defaultValue;
  }

  if (typeof defaultValue === "number") {
    return typeof incomingValue === "number" ? incomingValue : defaultValue;
  }

  if (typeof defaultValue === "boolean") {
    return typeof incomingValue === "boolean" ? incomingValue : defaultValue;
  }

  return incomingValue ?? defaultValue;
}

export function normalizeContent(value) {
  return mergeWithDefaults(defaultContent, value);
}

export function loadStoredContent() {
  if (typeof window === "undefined") {
    return cloneData(defaultContent);
  }

  try {
    const storedValue = window.localStorage.getItem(CONTENT_STORAGE_KEY);

    if (!storedValue) {
      return cloneData(defaultContent);
    }

    return normalizeContent(JSON.parse(storedValue));
  } catch (error) {
    return cloneData(defaultContent);
  }
}

